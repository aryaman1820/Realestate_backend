package com.aryaman.realestate.model;

public class DashboardResponse {
	
	private long customers;
	private long projectdetails;
	private long builders;
	private long properties;
	private long appointments;
	
	
	public long getCustomers() {
		return customers;
	}
	public void setCustomers(long customers) {
		this.customers = customers;
	}
	public long getProjectdetails() {
		return projectdetails;
	}
	public void setProjectdetails(long projectdetails) {
		this.projectdetails = projectdetails;
	}
	public long getBuilders() {
		return builders;
	}
	public void setBuilders(long builders) {
		this.builders = builders;
	}
	public long getProperties() {
		return properties;
	}
	public void setProperties(long properties) {
		this.properties = properties;
	}
	public long getAppointments() {
		return appointments;
	}
	public void setAppointments(long appointments) {
		this.appointments = appointments;
	}
	
	

}
